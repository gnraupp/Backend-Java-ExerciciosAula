package com.stefanini.Aula5;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		// == - igual
		// != - diferente
		// < - menor
		// > - maior
		// <= - menor igual
		// >= - maior igual
		
		System.out.println(a == b);
	}

}
